package c1l112artropodes;

public class Artropodo {
    private int total;
    private int patas;

    public int getTotal() {
        return total;
    }

    public int getPatas() {
        return patas;
    }

    public Artropodo(int total, int patas) {
        this.total = total;
        this.patas = patas;
    }
}
