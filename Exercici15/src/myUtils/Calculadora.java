package myUtils;

public class Calculadora {
    int id;

    public float sumar(float p1, float p2){
        return (p1 + p2);
    }
    public float restar(float p1, float p2){
        return (p1 - p2);
    }
    public float multiplicar(float p1, float p2){
        return (p1 * p2);
    }
    public float dividir(float p1, float p2){
        return (p1 / p2);
    }

}
